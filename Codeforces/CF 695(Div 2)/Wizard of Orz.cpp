#include<bits/stdc++.h>

using namespace std;

typedef long long ll;

int main()
{
    ll t;
    cin>>t;
    while(t--)
    {
        ll n,x=0;
        cin>>n;
        if(n==1) {cout<<9<<endl; continue;}
        if(n==2) {cout<<98<<endl; continue;}
        if(n==3) {cout<<989<<endl; continue;}
        cout<<989;
        for(ll i=3;i<n;i++)
        {
            cout<<x;
            x++;
            if(x==10) x=0;
        }
        cout<<endl;
    }
    return 0;
}
